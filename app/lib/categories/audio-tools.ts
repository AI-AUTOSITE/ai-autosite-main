// ========================================
// Helper Functions
// ========================================

/**
 * Get all live audio tools
 */
export function getLiveAudioTools(): Tool[] {
  return audioTools.filter(tool => tool.status === 'live');
}

/**
 * Get all coming soon audio tools
 */
export function getComingAudioTools(): Tool[] {
  return audioTools.filter(tool => tool.status === 'coming');
}

/**
 * Get audio tools by tag
 */
export function getAudioToolsByTag(tag: string): Tool[] {
  return audioTools.filter(tool => tool.tags?.includes(tag));
}

/**
 * Get converter tools
 */
export function getConverterTools(): Tool[] {
  return audioTools.filter(tool => tool.tags?.includes('converter'));
}

/**
 * Get editor tools
 */
export function getEditorTools(): Tool[] {
  return audioTools.filter(tool => 
    tool.tags?.some(tag => ['trim', 'cut', 'merge', 'fade', 'loop', 'reverse'].includes(tag))
  );
}
